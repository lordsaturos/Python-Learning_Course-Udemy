# Estudio de Strings
# Strings Los strings en Python son un tipo de dato formado por cadenas (o secuencias) de caracteres de cualquier tipo, 
# formando un texto. concatenación Unificación de cadenas de texto: print("Hola" + " " + "mundo") >> Hola mundo caracteres 
# especiales Indicamos a la consola que el caracter a continuación del símbolo \ debe ser tratado como un caracter especial.
# \" > Imprime comillas \n > Separa texto en una nueva linea \t > Imprime un tabulador \\ > Imprime la barra invertida 
# textualmentePython TOTAL

print('100 + 40 + 30'); # Escribimos por pantalla "100 + 40 + 30".

print("Hola" + 'Fede'); # Escribimos por pantalla "HolaFede".
print("Hola" + " " + 'Fede'); # Escribimos por pantalla "Hola Fede".
print("Hola" + " " + 'Fede' + " " + "¿cómo estás?"); # Escribimos por pantalla "Hola Fede ¿cómo estás?".

print("Hola, me llamo \'Raúl\'"); # Escribimos por pantalla "Hola, me llamo 'Raúl'".

print("Esta es una linea\nY esta es otra linea"); # Escribimos por pantalla "Esta es una linea" y "Y esta es otra linea" en dos lineas diferentes.
print("\tEsta es la tercera linea") # Escribimos por pantalla "Esta es la tercera linea" con un tabulador al principio.

print('This isn\'t a number'); # Escribimos por pantalla "This isn't a number".

print("Este signo \\ es una barra invertida"); # Escribimos por pantalla "Este signo \ es una barra invertida".

